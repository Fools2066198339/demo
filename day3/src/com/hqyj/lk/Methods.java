public class Methods {
    public static int getSum(int i, int j, int k) {
        return i + j + k;
    }

    public static int getSum(int i, int j) {
        return i + j;
    }

    public static double getSum(double i, int j) {
        return i + j;
    }

    public static double getSum(int i, double j) {
        return i + j;
    }
}
